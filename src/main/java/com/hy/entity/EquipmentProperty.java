package com.hy.entity;

public class EquipmentProperty {
    private String equipmentid;
    private String propertydes;
    private String price;
    private String cnt;
    private String createtime;
    private String createuser;
    private String updatetime;
    private String updateuser;
    private String crondate;

    public String getEquipmentid() {
        return equipmentid;
    }

    public void setEquipmentid(String equipmentid) {
        this.equipmentid = equipmentid;
    }

    public String getPropertydes() {
        return propertydes;
    }

    public void setPropertydes(String propertydes) {
        this.propertydes = propertydes;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getCnt() {
        return cnt;
    }

    public void setCnt(String cnt) {
        this.cnt = cnt;
    }

    public String getCreatetime() {
        return createtime;
    }

    public void setCreatetime(String createtime) {
        this.createtime = createtime;
    }

    public String getCreateuser() {
        return createuser;
    }

    public void setCreateuser(String createuser) {
        this.createuser = createuser;
    }

    public String getUpdatetime() {
        return updatetime;
    }

    public void setUpdatetime(String updatetime) {
        this.updatetime = updatetime;
    }

    public String getUpdateuser() {
        return updateuser;
    }

    public void setUpdateuser(String updateuser) {
        this.updateuser = updateuser;
    }

    public String getCrondate() {
        return crondate;
    }

    public void setCrondate(String crondate) {
        this.crondate = crondate;
    }
}
