package com.hy.entity;

public class GoldLog {
    private String id;
    private String type;
    private String subtype;
    private String quantity;
    private String beforequantity;
    private String afterquantity;
    private String playerid;
    private String operationtime;
    private String operationuser;

    private String equipmentlogid;
    private String remark;

    private String playername;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getSubtype() {
        return subtype;
    }

    public void setSubtype(String subtype) {
        this.subtype = subtype;
    }

    public String getQuantity() {
        return quantity;
    }

    public void setQuantity(String quantity) {
        this.quantity = quantity;
    }

    public String getBeforequantity() {
        return beforequantity;
    }

    public void setBeforequantity(String beforequantity) {
        this.beforequantity = beforequantity;
    }

    public String getAfterquantity() {
        return afterquantity;
    }

    public void setAfterquantity(String afterquantity) {
        this.afterquantity = afterquantity;
    }

    public String getPlayerid() {
        return playerid;
    }

    public void setPlayerid(String playerid) {
        this.playerid = playerid;
    }

    public String getOperationtime() {
        return operationtime;
    }

    public void setOperationtime(String operationtime) {
        this.operationtime = operationtime;
    }

    public String getOperationuser() {
        return operationuser;
    }

    public void setOperationuser(String operationuser) {
        this.operationuser = operationuser;
    }

    public String getEquipmentlogid() {
        return equipmentlogid;
    }

    public void setEquipmentlogid(String equipmentlogid) {
        this.equipmentlogid = equipmentlogid;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

    public String getPlayername() {
        return playername;
    }

    public void setPlayername(String playername) {
        this.playername = playername;
    }
}
